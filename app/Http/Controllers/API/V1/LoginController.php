     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        $request->validate([
          'email_customer' => 'required',
          'password_customer' => 'required'
        ]);
        $result = Customer::where([['email_customer', '=', $request['email_customer']],['password_customer', '=', $request['password_customer']]])->first();
        if($result != null){
          return new LoginResource($result);
        }else {
          return Response::json(['status' => 'email or password not true'], 405);
        }
    }


